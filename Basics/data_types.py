# data_types.py
# Demonstrating basic data types in Python

# Integer
x = 10
print("Integer:", x, type(x))

# Float
y = 3.14
print("Float:", y, type(y))

# String
text = "Python"
print("String:", text, type(text))

# Boolean
flag = False
print("Boolean:", flag, type(flag))

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits, type(fruits))

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates, type(coordinates))

# Dictionary
person = {"name": "Alice", "age": 21}
print("Dictionary:", person, type(person))

# Set
unique_numbers = {1, 2, 3, 2}
print("Set:", unique_numbers, type(unique_numbers))

# Demonstrating different operators in Python

a = 10
b = 3

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** b)

# Comparison Operators
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater than:", a > b)
print("Less than:", a < b)
print("Greater or Equal:", a >= b)
print("Less or Equal:", a <= b)

# Logical Operators
x, y = True, False
print("AND:", x and y)
print("OR:", x or y)
print("NOT:", not x)

# Assignment Operators
c = 5
c += 2   # c = c + 2
print("Assignment (+=):", c)
